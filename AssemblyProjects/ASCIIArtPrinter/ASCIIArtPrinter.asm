; Discussion Section: AD8
; This program attains the purpose of printing ascii characters in a large 8-character by 16-character font.
; This program takes characters specified at memory locations x5000 for the 'zero'-bit or negative characters and x5001
; for the 'one'-bit or positive characters. It then draws an ascii character specified at memory location x5002 using the 
; FONT_DATA at the end of this program, using the first 2 hex characters, or the first 8 binary bits.

			.ORIG x3000 ; start program at memory location x3000
			AND R0, R0, #0 ;register serves to hold ascii values as OUT function only prints what is in R0.
			AND R1, R1, #0 ;register serves as counter for column, bit by bit
			AND R2, R2, #0 ;register serves as counter for row, line by line
			AND R3, R3, #0 ;register serves to hold the address of FONT_DATA
			AND R4, R4, #0 ;register serves to hold font data for each line, as we progress through the lines
			AND R6, R6, #0 ;register serves to hold the ascii value of the char that we need to print
			ADD R2, R2, #8 ;add half of counter since we cannot add full 16 with one function
			ADD R2, R2, #8 ;add second half of counter for reason stated above
			
			LDI R6, SPECCHAR ;load value of char that we need to print into R6

			ADD R6, R6, R6; multiply R6 by 2
			ADD R6, R6, R6; multiply R6 by 2
			ADD R6, R6, R6; multiply R6 by 2
			ADD R6, R6, R6; multiply R6 by 2. This serves to shift the hex value of the char by 1 so that we can add it to the font value and reach the encoding for the desired ascii character

			LEA R3, FONT_DATA ; loads the address of FONT_DATA into R3
			ADD R6, R3, R6; Adds the left-shifted hex value to the FONT_DATA to reach the desired instruction set for the specified ascii character. Stored in R6


NEXT_ROW	ADD R2, R2, #0 ;sets up encoding for the row. This statement is arbitrary, just to set CC for the next statement
			BRz DONE ; tests if above statement sets cc to zero (i.e. R2 holds value of 0). If so, ends program
			AND R1, R1, #0; Clears counter for bit-by-bit drawing of chars, since the font has a width of 8.
			ADD R1, R1, #8; Adds 8 to counter, thereby resetting it, since font has a width of 8
			LDR R4, R6, #0; Loads the memory address for the beginning of the instructions for the specified ascii charater, temporarily, in R4 

NEXT_COLUMN	ADD R1, R1, #0; sets up encoding for the columns. This statement is arbitrary just to set cc for next statement
			BRz ENDROW ; tests if above statement sets cc to zero (i.e. R2 holds value of 0). if so, moves to end_row statement to be able to make the next row
			ADD R4, R4, #0; another arbitrary statement to set cc for next statement
			BRn ONEBITS ; if above statement is negative (i.e. MSB is 1) move to ONEBITS block of code
			BRzp ZEROBITS ; if above statement is positive or zero (i.e. MSB is 0) move to ZEROBITS block of code

ZEROBITS	LDI R0, ZERO ; ZEROBITS BLOCK OF CODE. only if value of R4 has MSB equal to 0. This loads the ZERO ascii character specified at memory location x5000 into R0
			OUT ; Prints ascii character located at R0. in other words. prints ZERO ascii character
			ADD R4, R4, R4; Shift value stored in R4 one to the left to test the next value
			ADD R1, R1, #-1; Decrement column counter by 1
			BRzp NEXT_COLUMN ; Branch statement to go to next column block of code to reiterate this loop

ONEBITS		LDI R0, ONE ; ONEBITS block of code. Only if value of R4 has MSB equal to 1. This loads the ONE acii character specified at memory location x5001 into R0
			OUT ; Prints ascii character located at R0. In other words. Prints ONE ascii character
			ADD R4, R4, R4; Shift value stored in R4 one to the left to test the next value
			ADD R1, R1, #-1; Decrement column counter by 1
			BRzp NEXT_COLUMN ; Branch statement to go to next column block of code to reiterate this loop

ENDROW		LD R0, NEWLINE ; sets up encoding to end line. essentially to go to the next line and set up NEXT_ROW block of code. Assigns new line character to R0
			OUT ; Prints new line character.
			ADD R2, R2, #-1 ; decrements row counter by 1
			ADD R6, R6, #1 ; increments instruction counter for ascii character so that once we go to the next line, it runs the next set of 8-bit instructions
			BRnzp NEXT_ROW ; Branches to go to NEXT-ROW block of code


DONE 		HALT ; halts the program
NEWLINE		.FILL x0A ; character to start at new line
ZERO		.FILL x5000 ; character assigned to the '0' bits and should be printed if font data is 0
ONE			.FILL x5001 ; character assigned to the '1' bits and should be printed if font data is 1
SPECCHAR	.FILL x5002 ; character assigned to be printed in the 8 by 16 size font

FONT_DATA	;font data. 4096 lines for all 256 ascii characters		
